#!/usr/bin/env python3
"""
Mesh Import Pipeline -- UModel -> glTF -> Blender -> FBX -> UE4

Phase 1: UModel exports all StaticMesh + SkeletalMesh from game paks as glTF
Phase 2: Blender headless batch-converts glTF -> FBX
Phase 3: UE4 Editor imports FBX files into the project

Usage:
    python mesh_import.py --phase 1              # Export meshes via UModel
    python mesh_import.py --phase 2              # Convert glTF -> FBX via Blender
    python mesh_import.py --phase 3              # Import FBX into UE4 project
    python mesh_import.py --phase all            # Run all phases
    python mesh_import.py --status               # Show current state
    python mesh_import.py --phase 1 --dry-run    # Preview without executing
"""


# =============================================================================
def phase2_blender_convert(dry_run=False, batch_size=50):
    """Convert all glTF files to FBX via Blender headless."""
    print("=" * 70)
    print("PHASE 2: Blender Convert (glTF -> FBX)")
    print("=" * 70)

    if not BLENDER.exists():
        print(f"ERROR: Blender not found at {BLENDER}")
        return False

    if not BLENDER_SCRIPT.exists():
        print(f"ERROR: Blender script not found at {BLENDER_SCRIPT}")
        return False

    # Find all glTF files
    gltf_files = _fast_find(GLTF_EXPORT_DIR, ".gltf")
    print(f"Found {len(gltf_files)} glTF files")

    if not gltf_files:
        print("No glTF files to convert. Run Phase 1 first.")
        return False

    FBX_DIR.mkdir(parents=True, exist_ok=True)

    # Determine which ones still need conversion
    to_convert = []
    already_done = 0
    for gltf_path in gltf_files:
        # Mirror directory structure from gltf export to fbx dir
        rel = gltf_path.relative_to(GLTF_EXPORT_DIR)
        fbx_path = FBX_DIR / rel.with_suffix(".fbx")
        if fbx_path.exists():
            already_done += 1
        else:
            to_convert.append((gltf_path, fbx_path))

    print(f"  Already converted: {already_done}")
    print(f"  To convert: {len(to_convert)}")

    if dry_run:
        print(f"\n[DRY RUN] Would convert {len(to_convert)} files")
        return True

    if not to_convert:
        print("All files already converted!")
        return True

    # Write a batch Blender script that processes multiple files per launch
    # (launching Blender per-file is slow due to startup overhead)
    batch_script = SCRIPT_DIR / "blender_batch_gltf_to_fbx.py"
    batch_script_content = '''"""Blender batch script: convert multiple glTF files to FBX.
Usage: blender --background --python this_script.py -- manifest.json
"""
import sys
import json
import os
import bpy

argv = sys.argv[sys.argv.index("--") + 1:]
manifest_path = argv[0]

with open(manifest_path, "r") as f:
    pairs = json.load(f)

total = len(pairs)
for i, (gltf_in, fbx_out) in enumerate(pairs, 1):
    try:
        # Reset scene
        bpy.ops.wm.read_homefile(use_empty=True)

        # Import glTF
        bpy.ops.import_scene.gltf(filepath=gltf_in)

        # Ensure output directory exists
        os.makedirs(os.path.dirname(fbx_out), exist_ok=True)

        # Export FBX -- UE4-compatible settings
        bpy.ops.export_scene.fbx(
            filepath=fbx_out,
            axis_forward='-Z',
            axis_up='Y',
            use_selection=False,
            global_scale=1.0,
            apply_unit_scale=True,
            apply_scale_options='FBX_SCALE_ALL',
            mesh_smooth_type='FACE',
            add_leaf_bones=False,
        )

        if i % 25 == 0 or i == total:
            print(f"PROGRESS: {i}/{total} converted", flush=True)

    except Exception as e:
        print(f"ERROR converting {gltf_in}: {e}", flush=True)

print(f"BATCH COMPLETE: {total} files processed")
'''
    with open(batch_script, "w") as f:
        f.write(batch_script_content)

    # Process in batches to avoid memory issues
    converted = 0
    errors = 0
    total_batches = (len(to_convert) + batch_size - 1) // batch_size

    for batch_idx in range(total_batches):
        start = batch_idx * batch_size
        end = min(start + batch_size, len(to_convert))
        batch = to_convert[start:end]

        print(f"\nBatch {batch_idx + 1}/{total_batches} ({len(batch)} files)...")

        # Write manifest
        manifest_path = REPO_ROOT / "tools" / "blender-manifest.json"
        pairs = [[str(g), str(f)] for g, f in batch]
        with open(manifest_path, "w") as mf:
            json.dump(pairs, mf)

        cmd = [
            str(BLENDER),
            "--background",
            "--python", str(batch_script),
            "--", str(manifest_path)
        ]

        t0 = time.time()
        proc = subprocess.run(
            cmd,
            capture_output=True,
            text=True,
            errors="replace",
            timeout=600  # 10 min per batch
        )
        elapsed = time.time() - t0

        # Count results
        batch_converted = 0
        for _, fbx_path in batch:
            if fbx_path.exists():
                batch_converted += 1
        batch_errors = len(batch) - batch_converted

        converted += batch_converted
        errors += batch_errors
        print(f"  Done in {elapsed:.1f}s -- {batch_converted} converted, {batch_errors} errors")

        if proc.returncode != 0 and batch_errors == len(batch):
            print(f"  WARNING: Blender exited with code {proc.returncode}")
            # Print last few lines of output for debugging
            for line in (proc.stdout or "").splitlines()[-5:]:
                print(f"    {line}")

    print(f"\n{'=' * 70}")
    print(f"Blender conversion complete.")
    print(f"  Converted: {converted}")
    print(f"  Errors: {errors}")
    print(f"  Total FBX files: {converted + already_done}")

    return errors == 0



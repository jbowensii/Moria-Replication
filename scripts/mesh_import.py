def phase3_ue4_import(dry_run=False, batch_size=100):
    """Import FBX files into UE4 via Editor commandlet.

    Groups FBX files by destination directory, then imports each group
    with a single UE4 editor launch per group.

    To minimize editor launches (each takes ~30-60s startup), we batch
    multiple destination directories into a single ImportAssets call
    by mirroring the directory structure under a temp source folder.
    """
    print("=" * 70)
    print("PHASE 3: UE4 FBX Import")
    print("=" * 70)

    if not UE4_CMD.exists():
        print(f"ERROR: UE4 Editor not found at {UE4_CMD}")
        return False

    # Find all FBX files
    fbx_files = _fast_find(FBX_DIR, ".fbx")
    print(f"Found {len(fbx_files)} FBX files")

    if not fbx_files:
        print("No FBX files to import. Run Phase 2 first.")
        return False

    # Check which are already imported
    to_import = []
    already_done = 0
    for fbx_path in fbx_files:
        rel = fbx_path.relative_to(FBX_DIR)
        parts = list(rel.parts)
        if parts[0] == "Game":
            parts = parts[1:]
        uasset_rel = Path(*parts).with_suffix(".uasset")
        uasset_path = CONTENT_DIR / uasset_rel

        if uasset_path.exists():
            already_done += 1
        else:
            # Store: (fbx_path, relative_content_path_with_fbx_name)
            to_import.append((fbx_path, Path(*parts)))

    print(f"  Already imported: {already_done}")
    print(f"  To import: {len(to_import)}")

    if dry_run:
        print(f"\n[DRY RUN] Would import {len(to_import)} FBX files")
        for fbx, rel in to_import[:10]:
            dest = "/Game/" + str(rel.parent).replace("\\", "/")
            print(f"  {fbx.name} -> {dest}")
        if len(to_import) > 10:
            print(f"  ... and {len(to_import) - 10} more")
        return True

    if not to_import:
        print("All meshes already imported!")
        return True

    # Strategy: Group by destination directory, import one dir at a time.
    # Each ImportAssets call: -source=<temp_dir> -dest=/Game/<path>
    by_dest = {}
    for fbx_path, rel_path in to_import:
        dest = "/Game/" + str(rel_path.parent).replace("\\", "/")
        if dest not in by_dest:
            by_dest[dest] = []
        by_dest[dest].append(fbx_path)

    print(f"  Unique destination dirs: {len(by_dest)}")

    imported = 0
    errors = 0
    dir_count = 0
    total_dirs = len(by_dest)
    import shutil

    for dest_path, fbx_list in sorted(by_dest.items()):
        dir_count += 1

        # Create temp directory with just these FBX files
        temp_dir = REPO_ROOT / "tools" / "fbx-import-temp"
        if temp_dir.exists():
            shutil.rmtree(temp_dir, ignore_errors=True)
        temp_dir.mkdir(parents=True, exist_ok=True)

        for fbx in fbx_list:
            shutil.copy2(fbx, temp_dir / fbx.name)

        cmd = [
            str(UE4_CMD),
            str(UPROJECT),
            "-run=ImportAssets",
            f"-source={temp_dir}",
            f"-dest={dest_path}",
            "-nosourcecontrol",
            "-nullrhi",
            "-log",
        ]

        log_path = REPO_ROOT / "tools" / f"fbx-import-dir-{dir_count}.log"
        t0 = time.time()

        print(f"\n[{dir_count}/{total_dirs}] {dest_path} ({len(fbx_list)} files)...", flush=True)

        try:
            with open(log_path, "w") as log_f:
                proc = subprocess.run(
                    cmd,
                    stdout=log_f,
                    stderr=subprocess.STDOUT,
                    timeout=600,  # 10 min per directory
                )
            elapsed = time.time() - t0

            # Count successful imports
            dir_imported = 0
            for fbx in fbx_list:
                rel = fbx.relative_to(FBX_DIR)
                parts = list(rel.parts)
                if parts[0] == "Game":
                    parts = parts[1:]
                check_path = CONTENT_DIR / Path(*parts).with_suffix(".uasset")
                if check_path.exists():
                    dir_imported += 1

            dir_errors = len(fbx_list) - dir_imported
            imported += dir_imported
            errors += dir_errors

            status = "OK" if dir_errors == 0 else f"{dir_errors} FAILED"
            print(f"  {dir_imported}/{len(fbx_list)} imported ({elapsed:.1f}s) [{status}]", flush=True)

        except subprocess.TimeoutExpired:
            print(f"  TIMEOUT", flush=True)
            errors += len(fbx_list)
        except Exception as e:
            print(f"  ERROR: {e}", flush=True)
            errors += len(fbx_list)

    # Cleanup temp
    temp_dir = REPO_ROOT / "tools" / "fbx-import-temp"
    if temp_dir.exists():
        shutil.rmtree(temp_dir, ignore_errors=True)

    print(f"\n{'=' * 70}")
    print(f"UE4 import complete.")
    print(f"  Imported: {imported}")
    print(f"  Errors: {errors}")
    print(f"  Total mesh assets in project: {imported + already_done}")

    return errors == 0



def show_status():
    """Show current state of mesh import pipeline."""
    print("=" * 70)
    print("MESH IMPORT PIPELINE -- STATUS")
    print("=" * 70)

    # Game paks
    print(f"\nGame paks: {GAME_PAKS}")
    if GAME_PAKS.exists():
        pak_files = list(GAME_PAKS.glob("*"))
        for p in pak_files:
            size_mb = p.stat().st_size / (1024 * 1024)
            print(f"  {p.name}: {size_mb:.1f} MB")
    else:
        print("  NOT FOUND")

    # UModel
    print(f"\nUModel: {UMODEL}")
    print(f"  {'EXISTS' if UMODEL.exists() else 'NOT FOUND'}")

    # Blender
    print(f"\nBlender: {BLENDER}")
    print(f"  {'EXISTS' if BLENDER.exists() else 'NOT FOUND'}")

    # Phase 1: glTF exports
    print(f"\nPhase 1 -- glTF exports: {GLTF_EXPORT_DIR}")
    if GLTF_EXPORT_DIR.exists():
        gltf_files = _fast_find(GLTF_EXPORT_DIR, ".gltf")
        print(f"  glTF files: {len(gltf_files)}")
    else:
        print("  Directory not created yet")

    # Phase 2: FBX conversions
    print(f"\nPhase 2 -- FBX files: {FBX_DIR}")
    if FBX_DIR.exists():
        fbx_files = _fast_find(FBX_DIR, ".fbx")
        print(f"  FBX files: {len(fbx_files)}")
    else:
        print("  Directory not created yet")

    # Phase 3: UE4 imports
    print(f"\nPhase 3 -- UE4 project: {CONTENT_DIR}")
    # Count mesh .uasset files in project
    mesh_count = 0
    sm_count = 0
    sk_count = 0
    if CONTENT_DIR.exists():
        for root, _, files in os.walk(CONTENT_DIR):
            for f in files:
                if f.endswith(".uasset"):
                    if f.startswith("SM_"):
                        sm_count += 1
                        mesh_count += 1
                    elif f.startswith("SK_"):
                        sk_count += 1
                        mesh_count += 1
        print(f"  StaticMesh (SM_): {sm_count}")
        print(f"  SkeletalMesh (SK_): {sk_count}")
        print(f"  Total meshes in project: {mesh_count}")

    # Game totals for comparison
    print(f"\nGame reference (extracted-assets):")
    try:
        ps_sm = subprocess.run(
            ["powershell", "-NoProfile", "-Command",
             f"(Get-ChildItem '{EXTRACTED_ASSETS}' -Filter 'SM_*.uasset' -Recurse -File).Count"],
            capture_output=True, text=True, timeout=60
        )
        ps_sk = subprocess.run(
            ["powershell", "-NoProfile", "-Command",
             f"(Get-ChildItem '{EXTRACTED_ASSETS}' -Filter 'SK_*.uasset' -Recurse -File).Count"],
            capture_output=True, text=True, timeout=60
        )
        game_sm = int(ps_sm.stdout.strip()) if ps_sm.stdout.strip() else 0
        game_sk = int(ps_sk.stdout.strip()) if ps_sk.stdout.strip() else 0
        print(f"  StaticMesh (SM_): {game_sm}")
        print(f"  SkeletalMesh (SK_): {game_sk}")
        print(f"  Total game meshes: {game_sm + game_sk}")
        if mesh_count > 0:
            pct = mesh_count / (game_sm + game_sk) * 100 if (game_sm + game_sk) > 0 else 0
            print(f"  Coverage: {pct:.1f}%")
    except Exception:
        print("  (Could not count)")

    print()


# =============================================================================
# Main
# =============================================================================
def main():
    parser = argparse.ArgumentParser(description="Mesh Import Pipeline")
    parser.add_argument("--phase", choices=["1", "2", "3", "all"], help="Which phase to run")
    parser.add_argument("--status", action="store_true", help="Show current status")
    parser.add_argument("--dry-run", action="store_true", help="Preview without executing")
    parser.add_argument("--batch-size", type=int, default=50, help="Blender/UE4 batch size")
    args = parser.parse_args()

    if args.status or not args.phase:
        show_status()
        if not args.phase:
            return

    if args.phase in ("1", "all"):
        ok = phase1_umodel_export(dry_run=args.dry_run)
        if not ok and args.phase == "all":
            print("Phase 1 had issues. Continuing anyway...")

    if args.phase in ("2", "all"):
        ok = phase2_blender_convert(dry_run=args.dry_run, batch_size=args.batch_size)
        if not ok and args.phase == "all":
            print("Phase 2 had issues. Continuing anyway...")

    if args.phase in ("3", "all"):
        ok = phase3_ue4_import(dry_run=args.dry_run, batch_size=args.batch_size)


if __name__ == "__main__":
    main()

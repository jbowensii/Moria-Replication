# Phase 1: UModel export all meshes as glTF
# =============================================================================
MESH_LIST_FILE = REPO_ROOT / "tools" / "mesh-asset-list.txt"
UMODEL_BATCH_SIZE = 100  # meshes per UModel invocation


def _load_mesh_list():
    """Load the mesh asset path list."""
    if not MESH_LIST_FILE.exists():
        print(f"Mesh list not found, generating...")
        subprocess.run(
            [sys.executable, str(SCRIPT_DIR / "gen_mesh_list.py")],
            cwd=str(REPO_ROOT), check=True
        )
    with open(MESH_LIST_FILE, "r") as f:
        return [l.strip() for l in f if l.strip()]


def phase1_umodel_export(dry_run=False):
    """Export all StaticMesh and SkeletalMesh from game paks via UModel."""
    print("=" * 70)
    print("PHASE 1: UModel Export (paks -> glTF)")
    print("=" * 70)

    if not UMODEL.exists():
        print(f"ERROR: UModel not found at {UMODEL}")
        return False

    if not GAME_PAKS.exists():
        print(f"ERROR: Game paks not found at {GAME_PAKS}")
        return False

    GLTF_EXPORT_DIR.mkdir(parents=True, exist_ok=True)

    # Load mesh asset list (generated by gen_mesh_list.py)
    mesh_paths = _load_mesh_list()
    print(f"Total mesh assets in game: {len(mesh_paths)}")

    # Check which are already exported
    to_export = []
    already_done = 0
    for mp in mesh_paths:
        gltf_path = GLTF_EXPORT_DIR / "Game" / (mp + ".gltf")
        if gltf_path.exists():
            already_done += 1
        else:
            to_export.append(mp)

    print(f"  Already exported: {already_done}")
    print(f"  To export: {len(to_export)}")
    print(f"  Output dir: {GLTF_EXPORT_DIR}")

    if dry_run:
        print(f"\n[DRY RUN] Would export {len(to_export)} meshes in batches of {UMODEL_BATCH_SIZE}")
        return True

    if not to_export:
        print("All meshes already exported!")
        return True

    # Process in batches -- UModel takes one primary package + multiple -pkg= args
    total_batches = (len(to_export) + UMODEL_BATCH_SIZE - 1) // UMODEL_BATCH_SIZE
    total_exported = 0
    total_errors = 0
    log_path = REPO_ROOT / "tools" / "umodel-export.log"

    with open(log_path, "w") as master_log:
        for batch_idx in range(total_batches):
            start = batch_idx * UMODEL_BATCH_SIZE
            end = min(start + UMODEL_BATCH_SIZE, len(to_export))
            batch = to_export[start:end]

            # First mesh is the primary package, rest are -pkg= args
            cmd = [
                str(UMODEL),
                "-game=ue4.27",
                f"-path={GAME_PAKS}",
                "-export",
                "-gltf",
                "-notex",
                "-nooverwrite",
                f"-out={GLTF_EXPORT_DIR}",
            ]
            # Add extra packages
            for extra in batch[1:]:
                cmd.append(f"-pkg={extra}")
            # Primary package last
            cmd.append(batch[0])

            t0 = time.time()
            print(f"\nBatch {batch_idx + 1}/{total_batches} ({len(batch)} meshes)...", flush=True)

            try:
                proc = subprocess.Popen(
                    cmd,
                    stdout=subprocess.PIPE,
                    stderr=subprocess.STDOUT,
                    text=True,
                    errors="replace",
                    bufsize=1
                )
                exported = 0
                errors = 0
                for line in proc.stdout:
                    master_log.write(line)
                    if line.startswith("Exporting StaticMesh") or line.startswith("Exporting SkeletalMesh"):
                        exported += 1
                    elif "ERROR" in line:
                        errors += 1
                        if total_errors + errors <= 20:
                            print(f"  ERROR: {line.strip()}", flush=True)

                proc.wait()
                elapsed = time.time() - t0
                total_exported += exported
                total_errors += errors
                print(f"  {exported} meshes exported in {elapsed:.1f}s (errors: {errors})", flush=True)

            except Exception as e:
                print(f"  EXCEPTION: {e}", flush=True)
                total_errors += len(batch)

    print(f"\n{'=' * 70}")
    print(f"UModel export complete.")
    print(f"  Meshes exported: {total_exported}")
    print(f"  Errors: {total_errors}")
    print(f"  Log: {log_path}")

    # Count actual glTF files produced
    gltf_files = _fast_find(GLTF_EXPORT_DIR, ".gltf")
    print(f"  glTF files on disk: {len(gltf_files)}")

    return total_exported > 0



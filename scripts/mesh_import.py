def _fast_find(directory: Path, extension: str = ".gltf") -> list:
    """Fast recursive file listing via PowerShell (OneDrive-friendly)."""
    dir_str = str(directory).replace("/", "\\")
    try:
        ps_cmd = (
            f"Get-ChildItem -Path '{dir_str}' -Filter '*{extension}' "
            f"-Recurse -File | ForEach-Object {{ $_.FullName }}"
        )
        result = subprocess.run(
            ["powershell", "-NoProfile", "-Command", ps_cmd],
            capture_output=True, text=True, errors="replace", timeout=300
        )
        if result.returncode == 0 and result.stdout.strip():
            return [Path(p) for p in result.stdout.strip().splitlines()]
    except Exception as e:
        print(f"  PowerShell find failed ({e}), falling back to os.walk")
    # Fallback
    found = []
    for root, _, files in os.walk(directory):
        for f in files:
            if f.endswith(extension):
                found.append(Path(root) / f)
    return found


def _count_mesh_assets():
    """Count SM_ and SK_ assets in extracted-assets (what exists in game)."""
    sm = 0
    sk = 0
    for root, _, files in os.walk(EXTRACTED_ASSETS):
        for f in files:
            if f.startswith("SM_") and f.endswith(".uasset"):
                sm += 1
            elif f.startswith("SK_") and f.endswith(".uasset"):
                sk += 1
    return sm, sk



UMODEL = REPO_ROOT / "tools" / "umodel" / "umodel_64.exe"
BLENDER = Path(r"C:\Program Files\Blender Foundation\Blender 5.0\blender.exe")
BLENDER_SCRIPT = SCRIPT_DIR / "blender_gltf_to_fbx.py"
GLTF_EXPORT_DIR = REPO_ROOT / "tools" / "umodel-meshes-gltf"
FBX_DIR = REPO_ROOT / "tools" / "umodel-meshes-fbx"
